package com.example.fruit.entity;

public class Fruit {
    private int fruit_id;
    private String fruit_name;
    private String fruit_describe;
    private String fruit_category;
    private String fruit_interest;
    private String fruit_image;

    public int getFruit_id() {
        return fruit_id;
    }

    public void setFruit_id(int fruit_id) {
        this.fruit_id = fruit_id;
    }

    public String getFruit_name() {
        return fruit_name;
    }

    public void setFruit_name(String fruit_name) {
        this.fruit_name = fruit_name;
    }

    public String getFruit_describe() {
        return fruit_describe;
    }

    public void setFruit_describe(String fruit_describe) {
        this.fruit_describe = fruit_describe;
    }

    public String getFruit_category() {
        return fruit_category;
    }

    public void setFruit_category(String fruit_category) {
        this.fruit_category = fruit_category;
    }

    public String getFruit_interest() {
        return fruit_interest;
    }

    public void setFruit_interest(String fruit_interest) {
        this.fruit_interest = fruit_interest;
    }

    public String getFruit_image() {
        return fruit_image;
    }

    public void setFruit_image(String fruit_image) {
        this.fruit_image = fruit_image;
    }
}
